package com.crowdar.performance.model;

public class Loops {
	private String pathFileData;
	private String methodsGenerateData;
	private Integer loops;
	private String percentageBusiness;
	public String getPathFileData() {
		return pathFileData;
	}
	public void setPathFileData(String pathFileData) {
		this.pathFileData = pathFileData;
	}
	public String getMethodsGenerateData() {
		return methodsGenerateData;
	}
	public void setMethodsGenerateData(String methodsGenerateData) {
		this.methodsGenerateData = methodsGenerateData;
	}
	public Integer getLoops() {
		return loops;
	}
	public void setLoops(Integer loops) {
		this.loops = loops;
	}
	public String getPercentageBusiness() {
		return percentageBusiness;
	}
	public void setPercentageBusiness(String percentageBusiness) {
		this.percentageBusiness = percentageBusiness;
	}
}
